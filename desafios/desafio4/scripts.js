// Función para mostrar "¡Hola, mundo!" en la consola
function holaMundo() {
    console.log("¡Hola, mundo!");
  }
  
  // Función para saludar con un nombre
  function saludar(nombre) {
    console.log("¡Hola, " + nombre + "!");
  }
  
  // Función para devolver el doble de un número
  function dobleNumero(numero) {
    return numero * 2;
  }
  
  // Función para calcular el promedio de tres números
  function promedioTresNumeros(num1, num2, num3) {
    return (num1 + num2 + num3) / 3;
  }
  
  // Función para encontrar el mayor de dos números
  function mayorEntreDos(num1, num2) {
    return num1 > num2 ? num1 : num2;
  }
  
  // Función para calcular el cuadrado de un número
  function cuadradoNumero(numero) {
    return numero * numero;
  }
  